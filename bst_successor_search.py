# Create a Binary Search Tree
bst  = BinarySearchTree()
keys = [5, 9, 11, 12, 14, 20, 25]
for k in keys:
    bst.insert(k)  

for i in range(len(keys)-1):
  node = bst.getNodeByKey(keys[i])
  successor = bst.getNodeByKey(keys[i+1])
  assert bst.find_in_order_successor(node) == successor

last_node = bst.getNodeByKey(keys[-1])
assert bst.find_in_order_successor(last_node) == None
